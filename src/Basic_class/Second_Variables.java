package Basic_class;

public class Second_Variables {
    public static void main(String[] args) {
        int money = 2000;
        money = 5000;
        System.out.println(money);
    }
}

// Naming convention of variable

/*
*  1. Variable names should not begin with numbers
*  2. Whitespace is not permitted in variables names
*  3. A java keyword (reversed word) cannot be used as a variable name
*  4. It is preferred to use variable names in camel case
*  5. When creating variables, it is preferred to give them meaningful name
*  6. All lowercase letters should be used when creating one-word variable name
*/

// Points to be remembered about identifiers

/*
 * 1. All identifiers should begin with a letter (A to Z or a to z), currency character($), or an underscore(_)
 * 2. After the first character, identifiers can have any combinations of the characters
 * 3. A keyword cannot be used as an identifiers
 * 4. The identifiers are case-sensitive
 * 5. Whitespace is not permitted
 */